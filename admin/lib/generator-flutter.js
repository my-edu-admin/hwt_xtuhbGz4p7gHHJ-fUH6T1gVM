function updateFlutterPubspec(pubspecPath, appData) {
  if (!fs.existsSync(pubspecPath)) {
    return;
  }

  const current = fs.readFileSync(pubspecPath, "utf8");
  const projectName = readYamlValue(current, "name") || "flutter_app";
  const sdkConstraint = readYamlValue(current, "sdk", true) || "^3.7.0";

  const next = `name: ${projectName}
description: "${escapeYaml(appData.app_name)}"
publish_to: "none"
version: 1.0.0+1

environment:
  sdk: ${sdkConstraint}

dependencies:
  flutter:
    sdk: flutter
  cupertino_icons: ^1.0.8
  google_fonts: ^6.2.1
  url_launcher: ^6.3.1
  video_player: ^2.9.2
  webview_flutter: ^4.10.0
  youtube_player_iframe: ^5.2.2

dev_dependencies:
  flutter_test:
    sdk: flutter
  flutter_lints: ^5.0.0

flutter:
  uses-material-design: true
`;

  fs.writeFileSync(pubspecPath, next, "utf8");
}

function readYamlValue(content, key, nested = false) {
  const pattern = nested
    ? new RegExp(`^\\s*${key}:\\s*(.+)$`, "m")
    : new RegExp(`^${key}:\\s*(.+)$`, "m");
  const match = String(content || "").match(pattern);
  return match ? String(match[1] || "").trim() : "";
}

