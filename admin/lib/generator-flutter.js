let cachedFlutterCommand = null;

function buildFlutterOutput(paths, business, appData) {
  fs.mkdirSync(paths.outputDir, { recursive: true });
  fs.mkdirSync(paths.apkDir, { recursive: true });

  ensureFlutterProject(paths, business, appData);
  fs.writeFileSync(paths.flutterMainPath, buildFlutterMainSource(business, appData), "utf8");
  fs.writeFileSync(paths.flutterManifestPath, buildAndroidManifest(appData), "utf8");
  updateFlutterPubspec(paths.flutterPubspecPath, appData);

  const build = runCommand(
    getFlutterCommand(),
    ["build", "apk", "--release"],
    paths.flutterProjectDir,
    20 * 60 * 1000
  );
  fs.writeFileSync(paths.apkLogPath, build.log, "utf8");

  if (build.status !== 0) {
    return {
      success: false,
      exit_code: build.status,
      apk_path: paths.apkPath,
      log_path: paths.apkLogPath,
      message: "Flutter build failed. Review the build log for details.",
      log_excerpt: trimLog(build.log),
    };
  }

  const generatedApkPath = path.join(
    paths.flutterProjectDir,
    "build",
    "app",
    "outputs",
    "flutter-apk",
    "app-release.apk"
  );

  if (!fs.existsSync(generatedApkPath)) {
    return {
      success: false,
      exit_code: build.status,
      apk_path: paths.apkPath,
      log_path: paths.apkLogPath,
      message: "Flutter reported success, but the APK file was not found.",
      log_excerpt: trimLog(build.log),
    };
  }

  fs.copyFileSync(generatedApkPath, paths.apkPath);
  return {
    success: true,
    exit_code: build.status,
    apk_path: paths.apkPath,
    log_path: paths.apkLogPath,
    message: "Flutter app built successfully.",
    log_excerpt: trimLog(build.log),
  };
}

function ensureFlutterProject(paths, business, appData) {
  const hasProject =
    fs.existsSync(paths.flutterPubspecPath) &&
    fs.existsSync(path.join(paths.flutterProjectDir, "android")) &&
    fs.existsSync(path.join(paths.flutterProjectDir, "lib"));

  if (hasProject) {
    return;
  }

  const projectName = buildFlutterProjectName(business);
  fs.mkdirSync(paths.outputDir, { recursive: true });

  const result = runCommand(
    getFlutterCommand(),
    [
      "create",
      "--platforms=android",
      "--project-name",
      projectName,
      "--org",
      "com.schooldnd.generated",
      "flutter_app",
    ],
    paths.outputDir,
    10 * 60 * 1000
  );

  if (result.status !== 0) {
    throw new Error(`Unable to create Flutter project.\n${trimLog(result.log)}`);
  }

  fs.writeFileSync(
    path.join(paths.flutterProjectDir, "README.md"),
    `# ${appData.app_name}\n\nGenerated by Generator Studio for ${business.name}.\n`,
    "utf8"
  );
}

function getFlutterCommand() {
  if (cachedFlutterCommand) {
    return cachedFlutterCommand;
  }

  const isWindows = process.platform === "win32";
  const directCandidates = [
    process.env.FLUTTER_BIN,
    process.env.FLUTTER_HOME
      ? path.join(process.env.FLUTTER_HOME, "bin", isWindows ? "flutter.bat" : "flutter")
      : "",
  ].filter(Boolean);

  for (const candidate of directCandidates) {
    if (fs.existsSync(candidate)) {
      cachedFlutterCommand = candidate;
      return cachedFlutterCommand;
    }
  }

  if (isWindows) {
    const probe = spawnSync("where.exe", ["flutter"], {
      encoding: "utf8",
      windowsHide: true,
    });
    const candidates = String(probe.stdout || "")
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean);
    const preferred =
      candidates.find((line) => line.toLowerCase().endsWith(".bat")) || candidates[0];
    if (preferred) {
      cachedFlutterCommand = preferred;
      return cachedFlutterCommand;
    }
  }

  cachedFlutterCommand = "flutter";
  return cachedFlutterCommand;
}

function buildFlutterProjectName(business) {
  const base = `${business.slug}_${String(business.id || "")
    .replace(/[^a-z0-9]+/gi, "_")
    .toLowerCase()}`;
  const normalized = base
    .replace(/[^a-z0-9_]+/g, "_")
    .replace(/^([^a-z]+)/, "app_")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "");
  return normalized.slice(0, 36) || "institute_app";
}

